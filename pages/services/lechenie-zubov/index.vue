                рассасываться.</p>
              <p>Врач должен вводить в канал опять повторно до момента сохранения в течение длительного промежутка
                времени снова кальция. Ввели кальций, оставили его там, прошло время (от одного дня до недели), кальций
                мы удаляем, насколько это возможно и замещаем его на материал, который не будет подвергаться
                рассасыванию. В основном, для этого используются <em>материалы-герметики</em> на основе эпоксидной
                смолы. Она биоинертна, в воде имеет небольшое расширение (это преимущество) до 4% объема усадки, что
                является плюсом.</p>
              <p>Остальным материалом, которым заполняют зубы, является наличие того, что называют <em>гуттаперчей</em>
