                одноклеточных органимов, которые существуют при Ph не здоровых тканей, а ослабленных, то есть кислых
                (окисленных).</p>
              <p>Для этого основным фактором лечения устойчивости является <span class="strong"><em>двухэтапное заполнение
                каналов</em></span>. В процессе работы врач должен обработать, сформировать корневые каналы. Они должны иметь
                только коническую форму, то есть от устья к верхушке они иметь должны форму конуса. Это говорит о
                правильной обработке и последующем правильном заполнении. Если это не отображается на снимках корневых
                каналов, ваш специалист достаточно неграмотен.</p>
              <p><span class="strong">Двухэтапность лечения в таком случае заключается в следующем:</span> механическая обработка, начальная
                химическая обработка, антисептическая обработка; сводится к определенным тем же воздействиям у всех
