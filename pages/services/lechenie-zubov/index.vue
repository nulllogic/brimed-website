                лечебного, поэтому врач должен ввести в корневые каналы после высушивания содержимое кальций-содержащих
                препаратов, то есть создать резко-щелочную среду, для того, чтобы за счет времени и наличия Ph эти
                микробы, грибы оттуда ушли или погибли.</p>
              <p>Кальций-содержащие препараты включают в себя негашеную известь (CaO). Также имеются включения йодоформа
                и тимола. Время воздействия: если происходит нейтрализация внутри зуба и восстановление щелочного
